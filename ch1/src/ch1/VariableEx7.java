package ch1;

public class VariableEx7 {
	public static void main(String[] args) {
	
		int a=10,b=3;
		System.out.println(a/b);  // java는 기본적으로 나눗셈은 몫만 출력된다.
		System.out.println(a%b);  // %는 나눗셈을 하고 남은 나머지를 표기해준다.
		double d=10.0;
		System.out.println(d/b); // 소수점을 보고 싶으면 하나는 부정정수(소숫점)이여야 한다
		
	}
}
